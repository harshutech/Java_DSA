public class switch_statment {
            public static void main(String args[]){

                int number = 4;

                switch(number){
                    case 1 : System.out.println("you pressed one");
                    break;

                     case 2 : System.out.println("you pressed two");
                    break;

                     case 3 : System.out.println("you pressed three");
                    break;

                     case 4 : System.out.println("you pressed four");
                    break;

                    default : System.out.println("empty");
                }

}
}

// this is an example if switch statment